/* ============================================================
 * openos - 用户态测试程序
 * 在 ring 3 执行的用户代码
 * ============================================================ */

#include "../include/syscall.h"
#include "../include/usermode.h"

/* 用户程序入口点 */
void user_entry(void)
{
    /* 在 VGA 屏幕底部输出 */
    const char *msg = "Hello from USER MODE!\n";
    
    /* 调用内核 SYS_WRITE */
    sys_write(msg, 23);
    
    /* 获取当前 PID */
    uint32_t pid = sys_getpid();
    
    /* 无限循环 - 用户程序不应该返回 */
    while (1) {
        /* 什么都不做 */
    }
}

/* ============================================================
 * 备用入口 - 简单打印后循环
 * ============================================================ */
void user_main(void)
{
    /* 简单的用户程序 */
    volatile int x = 0;
    while (x == 0) {
        x++;  /* 避免编译器优化掉这个循环 */
    }
}
