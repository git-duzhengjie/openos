#define GLYPH_ROW(ch, r0, r1, r2, r3, r4, r5, r6, r7) \
    case ch: \
        switch (row) { \
            case 0: return r0; \
            case 1: return r1; \
            case 2: return r2; \
            case 3: return r3; \
            case 4: return r4; \
            case 5: return r5; \
            case 6: return r6; \
            case 7: return r7; \
        } \
        return 0x00

